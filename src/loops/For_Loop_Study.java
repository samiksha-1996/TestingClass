package loops;

public class For_Loop_Study {

	public static void main(String[] args) {
		// I want to print 2 10 times
		//initialization--> int i
		//condition-->10
		//updation-->increment
		
		//syntax--> for(initiliazation;condition;updation)
		    //operation tast
		    //printing statement
		
		for(int a=1;a <= 10; a++)
		{
			System.out.println(2);
		}
		
		//I want to print table table of 3
		//ini-->int i
		//condition-->30
		//updation-->incremental
		
		for(int i =3 ; i <= 30; i=i+3) 
		{
			System.out.println(i);
		}
		
		
		
		//I want to print table of 3 in reverse
		//ini-->int i
		//condition-->30
		//updation-->decremental
		
		for(int i= 30; i>=3 ;i=i-3)
		{
			System.out.println(i);
		}
		
		//I want to print pune 5 times
		//ini-->1
		//updation-->incrementeal
		//condition-->5
		
		for(int i=1 ;i<=5 ;i++) 
		{
			System.out.println("Pune");
			
		}
		
		
		
		
		
		
		
			
		
		

	}

}
